package ex_01_helloworld;

public class Lab010_println_vs_print {
    public static void main(String[] args) {
        System.out.println("java class1");
        System.out.println("java class2");
        System.out.print("java3");
        System.out.print("java4");
        /**
         * #### println() vs print()
         * println -> new line in the console
         *
         * print -> will not add new line
         * this is console o/p
         * java class1
         * java class2
         * java3java4
         */
    }
}
