package ex_07_IncrementDecrement;

public class Lab072_ID {
    public static void main(String[] args) {
        int a = 10;
        int result = a++;
        System.out.println(a);
        System.out.println(result);

        int b = 12 ;
        int res = ++b;
        System.out.println(b);
        System.out.println(res);
    }
}
